package project.data;

/**
 * The Class that is all expected exceptions
 * that could happen when using the database.
 */
@SuppressWarnings("serial")
public class HardyException extends Exception {

	/**
	 * Instantiates a new database exception.
	 * Null constructor.
	 */
	public HardyException() { }

	/**
	 * Instantiates a new database exception.
	 *
	 * @param message the message
	 */
	public HardyException(String message) {
		super(message);
	}

	/**
	 * Instantiates a new database exception.
	 *
	 * @param cause the cause
	 */
	public HardyException(Throwable cause) {
		super(cause);
	}

	/**
	 * Instantiates a new database exception.
	 *
	 * @param message the message
	 * @param cause the cause
	 */
	public HardyException(String message, Throwable cause) {
		super(message, cause);
	}

}
